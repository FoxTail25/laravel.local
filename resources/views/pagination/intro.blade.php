    <h2>
        Пагинация в Laravel
    </h2>
    Пагинация в Laravel — это инструмент, который разбивает большой список данных (например, 10 000 товаров или постов)
    на маленькие, удобные страницы (например, по 15 штук на страницу).Вместо того чтобы загружать из базы данных сразу
    всё (от чего сервер может «лечь», а у пользователя зависнет браузер), Laravel загружает только кусочек для текущей
    страницы и сам рисует кнопки «Назад», «Вперед» и номера страниц.
    <br />
    <h5>Два главных вида пагинации в Laravel</h5>
    <b>paginate(10) - <i>(Обычная)</i></b>
    <br />
    <b>Как работает:</b> Делает два запроса к базе: первый считает общее количество
    записей, второй — забирает 10 штук для текущей страницы.Что видит юзер: Полную панель: «Назад [1] [2] 3 [4] [5]
    Вперед».
    <br />
    <b>Минус:</b> На
    таблицах в миллионы строк подсчет общего количества записей начинает сильно тормозить базу данных.
    <br /><br />
    <b>simplePaginate(10) - <i>(Упрощенная)</i></b>
    <br />
    <b>Как работает:</b> Не считает, сколько всего записей в базе. Она просто
    забирает 11 записей вместо 10. Если
    11-я запись есть — значит, существует «Следующая» страница.Что видит юзер: Только две кнопки: «Назад» и «Вперед»
    (как в ленте новостей или соцсетях).
    <br />
    <b>Плюс:</b> Работает молниеносно даже на гигантских базах данных.


    Как это устроено (на пальцах)Вся магия происходит
    всего в двух местах: в контроллере и в шаблоне (Blade).1. В Контроллере (выборка из базы)Вместо привычного метода
    ->get(), который забирает всё, вы вызываете метод ->paginate()
    <br />
    Посмотрим на конкретный пример:
    <pre>
class TestController extends Controller
{
    public function paginateTest(){
        $users = User::paginate(5);
        return view('test.paginate',['data'=> $users]);
    }
}</pre>
    Далее мы передаём этих пользвателей в представление (blade) test.paginate
    а в представлении уже выводим на экран следующим способом:
    <pre>
    &#64;foreach ($data as $user)
        &lt;p>&#123;&#123; $user->name }}&lt;/p>
    &#64;endforeach
    &#123;&#123; $data->links() }}
    </pre>
    Дальше (если у вас настроен TailWind) работает "магия Laravel" :)

    По умолчанию ссылки links() заточены под фреймворк Tailwind CSS. Если ваш сайт на Bootstrap, просто добавьте в метод
    boot() вашего App\Providers\AppServiceProvider
    <ol>
        <li>
            Шаг 1. Откройте нужный файл В редакторе кода (например, VS Code или PhpStorm) найдите и откройте файл по
            этому пути:📁 app/Providers/AppServiceProvider.php
        </li>
        <li>
            Шаг 2. Замените его содержимое Внутри этого файла найдите метод boot(). Изначально он обычно пустой. Вам
            нужно добавить туда всего одну строчку, а также подключить правильный класс в самом верху файла (через
            use).Вот как должен выглядеть ваш файл AppServiceProvider.php целиком:
            <pre>
namespace App\Providers;

use Illuminate\Support\ServiceProvider;
// 1. ОБЯЗАТЕЛЬНО ИМПОРТИРУЕМ КЛАСС ПАГИНАТОРА ВВЕРХУ ФАЙЛА:
use Illuminate\Pagination\Paginator;

class AppServiceProvider extends ServiceProvider
{
    /**
     * Register any application services.
     */
    public function register(): void
    {
        //
    }

    /**
     * Bootstrap any application services.
     */
    public function boot(): void
    {
        // 2. ДОБАВЛЯЕМ ЭТУ СТРОЧКУ ВНУТРЬ МЕТОДА boot():
        Paginator::useBootstrapFive();
    }
}
            </pre>
        </li>
    </ol>

    Вместо огромных
    сломанных кнопок Tailwind на экране появится аккуратная, красивая HTML-разметка, которая автоматически подхватит
    CSS-стили вашего Bootstrap. Кнопки станут правильного размера и выстроятся в красивый ряд.

