    {{--
public function index()
{
    // Запрашиваем из базы всего по 10 товаров за раз
    $products = Product::paginate(10);

    return view('products.index', compact('products'));
}
Используйте код с осторожностью.Laravel сам поймет, на какой странице находится пользователь (он смотрит на адресную строку, например: ://site.com). Он автоматически посчитает сдвиг (OFFSET) в базе данных и достанет нужные 10 товаров.2. В Blade-шаблоне (вывод на экран)Внутри цикла @foreach вы выводите товары как обычно. А вот чтобы внизу появились кнопки переключения страниц, достаточно вызвать один метод:html<div class="container">
    @foreach ($products as $product)
        <p>{{ $product->name }}</p>
    @endforeach

    <!-- Магия: эта строчка сама нарисует красивую HTML-пагинацию с ссылками -->
    {{ $products->links() }}
</div>

Используйте код с осторожностью.Два главных вида пагинации в Laravelpaginate(10) (Обычная)Как работает: Делает два запроса к базе: первый считает общее количество записей, второй — забирает 10 штук для текущей страницы.Что видит юзер: Полную панель: «Назад [1] [2] 3 [4] [5] Вперед».Минус: На таблицах в миллионы строк подсчет общего количества записей начинает сильно тормозить базу данных.simplePaginate(10) (Упрощенная)Как работает: Не считает, сколько всего записей в базе. Она просто забирает 11 записей вместо 10. Если 11-я запись есть — значит, существует «Следующая» страница.Что видит юзер: Только две кнопки: «Назад» и «Вперед» (как в ленте новостей или соцсетях). Плюс: Работает молниеносно даже на гигантских базах данных.

