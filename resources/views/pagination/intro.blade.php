    Пагинация в Laravel реализована одновременно просто и шикарно.
    В нашем расспоряжени есть модель User (связанная с таблицей users). Вот на её примере мы и рассмотри работу
    пагинации в Laravel
    <br />
    Допустим перед нами стоит задача отобразить список пользователей. Но выводить на экран не всех сразу. А по 5
    человек. Для начала, нам необходимо в контролеере получить пользователей. И ранее мы делали это при помощи
    <pre>User::get()</pre>
    или
    <pre>User::All()</pre>
    В таком случае мы получаем сразу весь список пользователей. Это удобно пока их у нас не много. Но если их десятки
    тысяч. То такой запрос может положить как сервер, так и браузер пользователя.
    По этому разумнее будет получать их по 3,5 или 10 записей.
    Для этого, в контролере, вместо вышеуказанных методов, мы будем использвоать
    <pre>User::paginate('количество пользователей')</pre>
    Вот так это выглядит в коде:
    <pre>
class TestController extends Controller
{
    public function paginateTest(){
        $users = User::paginate(5);
        return view('test.paginate',['data'=> $users]);
    }
}</pre>
    Далее мы передаём этих пользвателей в представление test.paginate
    а в представлении уже выводим на экран следующим способом:
    <pre>
    &#64;foreach ($data as $user)
        &lt;p>&#123;&#123; $user->name }}&lt;/p>
    &#64;endforeach
    &#123;&#123; $data->links() }}
    </pre>
    Дальше (если у вас настроен TailWind) работает "магия Laravel" :)

