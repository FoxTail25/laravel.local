    <h2>
        Введение в Eloquent Laravel
    </h2>
    При работе с базами данных в фреймворках часто используют подход, в котором каждой таблице в базе данных
    соответствует свой PHP класс (модель). При этом каждая запись в таблице будет представлять собой объект этого
    класса.
    <br />
    В результате получится, что мы будем обращаться к базе, вызывая методы соответствующего объекта. To есть получится
    некая ООП обертка над базой данных.
    <br />
    Такая обертка называется ORM - система объектно-реляционного отображения. В Laravel такая система называется
    Eloquent.
    <br />
    Каждая таблица базы данных имеет соответствующий класс-модель, который используется для работы с этой таблицей.
    Модели позволяют запрашивать данные из таблиц, а также вставлять в них новые записи.
    <br />
    Модели располагаются в папке app. Имя модели должно соответствовать имени таблицы. При этом имя таблице дается во
    множественном числе, а имя модели - в единственном и с большой буквы. Например, для таблицы posts вам нужно сделать
    модель Post.
    <h3>Основные фичи и преимущества Eloquent</h3>
    <ol>
        <li>
            🔗
            <h4>
                Простая работа со связями (Relationships)
            </h4>
            Больше не нужно вручную писать ->leftJoin('cities', ...). Связи прописываются прямо внутри класса модели в
            виде функций:
            <pre>
            // Внутри модели User
            public function city() {
                // Пользователь принадлежит городу
                return $this->belongsTo(City::class, 'city_id');
            }
            </pre>
            После этого вы можете получать город пользователя одной короткой командой (Laravel сам сделает Join «под
            капотом»):
            <pre>
            $user = User::find(1);
            echo $user->city->name; // Выведет название города!
            </pre>
        </li>
        <li>
            🛡️
            <h4>
                Безопасность из коробки (Мутаторы и Касты)
            </h4>
            Eloquent умеет автоматически трансформировать данные при записи или чтении.
            <h5>
                Кастинг (Casts):
            </h5>
            Можно указать, что поле is_active в базе данных — это число 0 или 1, но в PHP вы всегда хотите видеть его
            как true/false.
            <h5>
                Хеширование:
            </h5>
            Можно настроить модель так, чтобы при сохранении пароля $user->password = 'secret' Laravel автоматически
            шифровал его через bcrypt.
        </li>
        <li>
            🕒
            <h4>
                Автоматические даты (created_at и updated_at)
            </h4>
            Eloquent самостоятельно управляет полями времени создания и изменения записи. Вам не нужно вручную
            прописывать NOW() при создании строки.
        </li>
        <li>
            🗑️
            <h4>
                Мягкое удаление (Soft Deletes)
            </h4>
            Если включить эту фичу, то при вызове $user->delete() строка не удалится физически из базы данных. Вместо
            этого в поле deleted_at запишется текущее время. Для вашего приложения пользователь «исчезнет», но
            администратор всегда сможет его восстановить одной командой $user->restore().
        </li>
        <li>
            ⚡
            <h4>
                События моделей (Model Events)
            </h4>
            Вы можете «подслушивать» действия с моделью. Например: «когда создается новый пользователь (creating),
            автоматически сгенерировать ему уникальный токен» или «когда пользователь удаляется (deleted), автоматически
            удалить все его аватарки с сервера».
        </li>
    </ol>
    <h3>
        Главный минус Eloquent
    </h3>
    За удобство приходится платить скоростью и памятью. Eloquent создает тяжелые PHP-объекты для каждой строчки из базы.
    <ul>
        <li>
            Если вам нужно просто выбрать и отобразить 30 пользователей — Eloquent идеален.
        </li>
        <li>
            Если вам нужно перебрать 500 000 записей для сложного отчета или аналитики — чистый Query Builder
            (DB::table(...)) отработает в разы быстрее и потратит гораздо меньше оперативной памяти.
        </li>
    </ul>
