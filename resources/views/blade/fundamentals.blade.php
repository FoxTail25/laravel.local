    <h3>
        Введение в Blade
    </h3>
    В данном разделе мы рассмтривается шаблонизатор Blade, встроенный в Laravel. Этот шаблонизатор предоставляет нам
    набор специальных команд, которые мы можем использовать в файлах представлений. Таким образом мы можем избежать
    смешения HTML и PHP кода, а также получить некоторые дополнительные возможности.
    <br />
    <br />
    <h4>
        Вывод переменных в атрибуты в Blade в Laravel
