    <h3>
        Работа с массивами в Blade в Laravel
    </h3>
    Из действия в представление можно также передавать и массивы:
    <pre>
    // Controller code:
    return view('test', ['arr' => [1, 2]]);</pre>
    Давайте выведем элементы нашего массива на экран:
    <pre>
    первый элемент массива: &#123;&#123; $arr[0] &#125;&#125;
    второй элемент массива: &#123;&#123; $arr[1] &#125;&#125;</pre>
    Так как в фигурных скобках можно выполнять любой PHP код, то можно, например, найти сумму элементов массива:
    <pre>сумма: &#123;&#123; $arr[0] + $arr[1] &#125;&#125;</pre>
    Можно также найти количество элементов массива с помощью PHP функции count:
    <pre>сумма: &#123;&#123; count($arr) &#125;&#125;</pre>
