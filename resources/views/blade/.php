        <h3>
            Дерегива &#64;if в шаблонах Blade
        </h3>
        С помощью Blade директивы &#64;if можно задавать условия в представлениях. Давайте посмотрим на примере.
        <br />
        Пусть из действия в представление передается переменная, определяющая, авторизован пользователь или нет:
        <pre>
            return view('post.show', [
                'isAuth' => true
            ]);
            </pre>
        Покажем текст только для авторизованного пользователя:
        <pre>
            &#64;if ($isAuth)
                вы авторизованы
            &#64;endif
            </pre>
        А тепер покажем текст для неавторизованного пользователя:
        <pre>
            &#64;if (!$isAuth)
                вы не авторизованы
            &#64;endif
            </pre>
    </div>
