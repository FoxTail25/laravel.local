        <h3>
            Дерегива &#64;elseif в шаблонах Blade
        </h3>
        Следующий пример демонстрирует работу директивы &#64;elseif, работающий подобно своему PHP аналогу:
        <pre>
            &#64;if ($num > 0)
                больше нуля
            &#64;elseif($num === 0)
                ноль
            &#64;else
                меньше нуля
            &#64;endif
            </pre>
