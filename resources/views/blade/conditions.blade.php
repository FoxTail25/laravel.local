    <h4>
        Дерегива &#64;unless в шаблонах Blade
    </h4>
    Директива &#64;unless представляет собой команду <b>пока не</b> и не имеет аналога в PHP. Рассмотрим работу
    данной директивы на практическом примере.
    <br />
    Пусть в переменной $isAuth хранится пометка об авторизации пользователя. В том случае, если пользователь еще не
    авторизован, выведем ему сообщение об этом, пока используя директиву &#64;if:
    <pre>
    &#64;if (!$auth)
        вы еще не авторизованы
    &#64;endif</pre>
    Как вы видите, перед именем переменной нам приходится писать восклицательный знак, чтобы сделать отрицание.
    <br />
    Можно избавится от этого восклицательного знака, воспользовавшись директивой &#64;unless, которая сама по себе
    является отрицанием:
    <pre>
    &#64;unless ($isAuth)
        вы еще не авторизованы
    &#64;endunless</pre>
