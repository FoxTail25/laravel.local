        Сложные команды в условиях Blade в Laravel
    </h4>
    В условиях можно давать более сложные команды. Пусть, например, в представление передается массив. Давайте
    подсчитаем количество элементов в этом массиве и, в зависимости от количества, выведем разный текст:
    <pre>
    &#64;if (count($arr) >= 1)
        в массиве есть записи
    &#64;else
        в массиве нет записей
    &#64;endif</pre>
